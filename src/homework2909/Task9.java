package homework2909;

public class Task9 {
    public double summ(double arg1, double arg2){
        double res = arg1 + arg2;
        return res;}
    public double mult(double arg1, double arg2){
        double res = arg1*arg2;
        return res;}
    public double minus(double arg1, double arg2){
        double res = arg1 - arg2;
        return res;}
    public double div(double arg1, double arg2){
        double res = arg1/arg2;
        return res;
    }
}
